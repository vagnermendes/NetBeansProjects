    private String CalcularMp() {
        double c1 = Double.parseDouble(c_f4.getText().replace(",", "."));        
        double e = Double.parseDouble(c_e.getText().replace(",", "."));                
        double w = (c1*e);                
        return fmt.format(w);
    }

    private String CalcularSigma3() {
        double p = Double.parseDouble(c_f4.getText().replace(",", "."));        
        double e = Double.parseDouble(c_e.getText().replace(",", "."));   
        double area = (base*altura);   
        double mix = ((base*altura*altura*altura)/12);  
        double w1 = mix/Double.parseDouble(c_y1.getText().replace(",", "."));
        double w2 = ((mix/Double.parseDouble(c_y1.getText().replace(",", ".")))*-1);     
        double s = ((p/area)+(p*e)/w1)*10;                
        return fmt.format(s);
    }
    
    private String CalcularSigma4() {
        double p = Double.parseDouble(c_f4.getText().replace(",", "."));        
        double e = Double.parseDouble(c_e.getText().replace(",", "."));   
        double area = (base*altura);   
        double mix = ((base*altura*altura*altura)/12);  
        double w1 = mix/Double.parseDouble(c_y1.getText().replace(",", "."));
        double w2 = ((mix/Double.parseDouble(c_y1.getText().replace(",", ".")))*-1);     
        double s = ((p/area)+(p*e)/w2)*10;                
        return fmt.format(s);
    }

