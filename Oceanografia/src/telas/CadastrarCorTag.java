    private void SalvarCorTag(){
        cortag  = new CorTag();
        bd_cortag = new BD_CorTag(kernel);
        
        if(cortag_id.getText().equals("0")){
            cortag.setNome(cor_tag_nome.getText());
            if(!cor_tag_nome.getText().equals("")){
                bd_cortag.Salvar(cortag);
            }            
        }else{
            cortag.setNome(cor_tag_nome.getText());
            cortag.setCortag_id(Integer.parseInt(cortag_id.getText()));
            bd_cortag.Atualizar(cortag);
        }
        
        cor_tag_nome.setText(""); 
        cortag_id.setText("0"); 
        //PREENCHER A TABELA DE DADOS COM AS ÁREAS
        TabelaCorTag();
    }
    
    //TABELA COR TAG
    public void TabelaCorTag(){
        
        bd_cortag = new BD_CorTag(kernel);
        CorTag[] cortag_vetor = null;
        try {
            //VERIFICAR SE É BUSCA OU NÃO        
            cortag_vetor = bd_cortag.getCorTags();
        } catch (SQLException ex2) {
            Logger.getLogger(CadastrarCorTag.class.getName()).log(Level.SEVERE, null, ex2);
        }
        
        
        String dados[][] = new String[cortag_vetor.length][((acao==1)?5:4)];

        CorTag tag = new CorTag();
        for(int i=0; i < cortag_vetor.length; i++){
            tag = (CorTag)cortag_vetor[i];
            dados[i][0] = String.valueOf(tag.getCortag_id());
            dados[i][1] = String.valueOf(tag.getNome());            
            if(acao==1){                
                dados[i][editar] = "editar";
                if(kernel.getUsuario().getNivel()==0){        
                dados[i][excluir] = "excluir";
                }
                dados[i][selecionar] = "selecionar";
            }else{
                dados[i][editar] = "editar";
                if(kernel.getUsuario().getNivel()==0){        
                dados[i][excluir] = "excluir";
                }
            }            
         
        }
      
        
        tabela_cortag.setModel(new javax.swing.table.DefaultTableModel(
            dados,
            ((acao==1)?(new String [] {"Cod", "Temporada","","", ""}):(new String [] {"Cod", "Temporada","", ""}))){
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        });
      
        tabela_cortag.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabela_cortag.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabela_cortag.getColumnModel().getColumn(1).setPreferredWidth(300);        
        
        if(acao==1){
            tabela_cortag.getColumnModel().getColumn(editar).setPreferredWidth(30);
            if(kernel.getUsuario().getNivel()==0){        
            tabela_cortag.getColumnModel().getColumn(excluir).setPreferredWidth(30);
            }
            tabela_cortag.getColumnModel().getColumn(selecionar).setPreferredWidth(30);
        }else{
            tabela_cortag.getColumnModel().getColumn(editar).setPreferredWidth(30);
            if(kernel.getUsuario().getNivel()==0){        
            tabela_cortag.getColumnModel().getColumn(excluir).setPreferredWidth(30);
            }
        }
        
        tabela_cortag.setDragEnabled(true);
        jScrollPane2.setViewportView(tabela_cortag);        
        //ButtonColumn buttonColumn0 = new ButtonColumn(table, 0);//VER
        if(acao==1){
            ButtonColumnCorTag button1 = new ButtonColumnCorTag(tabela_cortag, editar);//EDITAR
            if(kernel.getUsuario().getNivel()==0){        
            ButtonColumnCorTag button2 = new ButtonColumnCorTag(tabela_cortag, excluir);//EDITAR
            }
            ButtonColumnCorTag button3 = new ButtonColumnCorTag(tabela_cortag, selecionar);//SELECIONAR
        }else{
            ButtonColumnCorTag button1 = new ButtonColumnCorTag(tabela_cortag, editar);//EDITAR
            if(kernel.getUsuario().getNivel()==0){        
            ButtonColumnCorTag button2 = new ButtonColumnCorTag(tabela_cortag, excluir);//EXCLUIR
            }
        }
    }
    
    //CLASSE BUTTONCOLUMNCORTAG
    class ButtonColumnCorTag extends AbstractCellEditor
         implements TableCellRenderer, TableCellEditor, ActionListener
     {
         JTable table;
         JButton renderButton;
         JButton editButton;
         String text;

         public ButtonColumnCorTag(JTable table, int column)
         {
             super();
             this.table = table;
             renderButton = new JButton();

             editButton = new JButton();
             
             editButton.setFocusPainted( false );
             editButton.addActionListener( this );

             TableColumnModel columnModel = table.getColumnModel();
             columnModel.getColumn(column).setCellRenderer( this );
             columnModel.getColumn(column).setCellEditor( this );
         }

         public Component getTableCellRendererComponent(
             JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column)
         {
             if (hasFocus)
             {
                 renderButton.setForeground(table.getForeground());
                 renderButton.setBackground(UIManager.getColor("Button.background"));
             }
             else if (isSelected)
             {
                 renderButton.setForeground(table.getSelectionForeground());
                 renderButton.setBackground(table.getSelectionBackground());
            }
             else
             {
                 renderButton.setForeground(table.getForeground());
                 renderButton.setBackground(UIManager.getColor("Button.background"));
             }

             renderButton.setBackground(new java.awt.Color(255, 255, 255));
             renderButton.setBorder(null);
             renderButton.setBorderPainted(false);
             renderButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

             text = (value == null) ? "" : value.toString();
             
             String imagem1 = "";
             if(text.equals("selecionar")){
                 imagem1 = "select";
             }else{
                 imagem1 = text.equals("excluir")?"lixeira":"editar";
             }
             
             renderButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icones/"+imagem1+".png")));
             return renderButton;
         }

         public Component getTableCellEditorComponent(
             JTable table, Object value, boolean isSelected, int row, int column)
         {
             text = (value == null) ? "" : value.toString();             
             String imagem2 = text.equals("excluir")?"lixeira":"editar";
             editButton.setBackground(new java.awt.Color(255, 255, 255));
             editButton.setBorder(null);
             editButton.setBorderPainted(false);             
             editButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icones/"+imagem2+".png")));
             editButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
             //editButton.setText( text );
             return editButton;
         }

         public Object getCellEditorValue()
         {
             return text;
        }

         public void actionPerformed(ActionEvent e)
         {
             fireEditingStopped();

             switch(table.getSelectedColumn()){
                 case excluir:
                     SolicitaConfirmacaoCorTag();
                     TabelaCorTag();
                 break;
                 case editar:
                     int id_reg = Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString());
                     cortag = bd_cortag.gettagID(id_reg);
                     cor_tag_nome.setText(cortag.getNome());
                     cortag_id.setText(String.valueOf(cortag.getCortag_id()));
                 break;
                 case selecionar:
                     kernel.Tela(15, kernel,Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString()),objeto,null);
                 break;    
                 default:
                     kernel.Tela(0, kernel, 0,null,null);
                 break;
             }
             //System.out.println( "DADOS : " + table.getSelectedRow()+" "+" "+table.getSelectedColumn()+" "+table.getColumnCount()+" "+table.getValueAt(table.getSelectedRow(),0));
             
         }
     }
    private void SolicitaConfirmacaoCorTag(){
     boolean escolha = false;

     Object[] options = {"Sim",
                    "Não"};
    int resposta = JOptionPane.showOptionDialog(this,
        "Desejas excluir o registro ?",
        "Exclusão",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE,
        null,     //do not use a custom Icon
        options,  //the titles of buttons
        options[1]);
     
     if (resposta == 0) {
       bd_cortag.Excluir(Integer.parseInt(tabela_cortag.getValueAt(tabela_cortag.getSelectedRow(),0).toString()));
     }
     
    }

