            /******************** SETANDO DADOS DA ANESTESIA ********************/            
            SimpleDateFormat horaAnestesiaFormat = new SimpleDateFormat("HH:mm");  
            obj_Anestesia = new Anestesia();
            
            obj_Anestesia.setQuantidade(Double.parseDouble((anestesia_quantidade.getText().equals(""))?"0.0":anestesia_quantidade.getText()));
            obj_Anestesia.setConcentracao(Double.parseDouble((anestesia_concentracao.getText().equals(""))?"0.0":anestesia_concentracao.getText()));

