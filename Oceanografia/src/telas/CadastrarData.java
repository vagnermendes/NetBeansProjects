    private void SalvarData(){
        obj_Data     = new Data();
        bd_Data  = new BD_Data(kernel);
        
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");  
        Date date = new Date(); 
            
        if(Data_id.getText().equals("0")){            
            String sDate = data_ano.getText()+"-"+data_mes.getText()+"-"+data_dia.getText();
            try {
                obj_Data.setData(dateFormat.parse(sDate));
            } catch (ParseException ex) {
                Logger.getLogger(CadastrarData.class.getName()).log(Level.SEVERE, null, ex);
            }
            if(!data_ano.getText().equals("")){
                bd_Data.Salvar(obj_Data);
            }
            
        }else{            
            String sDate = data_ano.getText()+"-"+data_mes.getText()+"-"+data_dia.getText();
            try {
                obj_Data.setData(dateFormat.parse(sDate));
            } catch (ParseException ex) {
                Logger.getLogger(CadastrarData.class.getName()).log(Level.SEVERE, null, ex);
            }
            obj_Data.setData_id(Integer.parseInt(Data_id.getText()));
            bd_Data.Atualizar(obj_Data);
        }
        
        data_dia.setText(""); 
        data_ano.setText(""); 
        data_mes.setText(""); 
        Data_id.setText("0"); 
        //PREENCHER A TABELA DE DADOS COM AS ÁREAS
        TabelaData();
    }
    
    //TABELA Data
    public void TabelaData(){
        
        bd_Data = new BD_Data(kernel);
        Data[] Data_vetor = null;
        try {
            //VERIFICAR SE É BUSCA OU NÃO        
            Data_vetor = bd_Data.getDatas();
        } catch (SQLException ex2) {
            Logger.getLogger(CadastrarData.class.getName()).log(Level.SEVERE, null, ex2);
        }
        
        
        String dados[][] = new String[Data_vetor.length][4];

        Data esp = new Data();
        for(int i=0; i < Data_vetor.length; i++){
            esp = (Data)Data_vetor[i];
            dados[i][0] = String.valueOf(esp.getData_id());   
            dados[i][1] = new SimpleDateFormat("dd/MM/yyyy").format(esp.getData());            
            dados[i][2] = "editar";
            if(kernel.getUsuario().getNivel()==0){        
            dados[i][3] = "excluir";            
            }
        }
      
        
        tabela_Data.setModel(new javax.swing.table.DefaultTableModel(
            dados,
            new String [] {"Cod", "Nome","", ""}){
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        });
      
        tabela_Data.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabela_Data.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabela_Data.getColumnModel().getColumn(1).setPreferredWidth(300);        
        tabela_Data.getColumnModel().getColumn(2).setPreferredWidth(30);
        if(kernel.getUsuario().getNivel()==0){        
        tabela_Data.getColumnModel().getColumn(3).setPreferredWidth(30);
        }
        
        tabela_Data.setDragEnabled(true);
        sp.setViewportView(tabela_Data);        
        //ButtonColumn buttonColumn0 = new ButtonColumn(table, 0);//VER
        ButtonColumnData buttonColumnData1 = new ButtonColumnData(tabela_Data, 2);//EDITAR
        if(kernel.getUsuario().getNivel()==0){        
        ButtonColumnData buttonColumnData2 = new ButtonColumnData(tabela_Data, 3);//EXCLUIR
        }
    }

    //CLASSE BUTTONCOLUMN Data
    class ButtonColumnData extends AbstractCellEditor
         implements TableCellRenderer, TableCellEditor, ActionListener
     {
         JTable table;
         JButton renderButton;
         JButton editButton;
         String text;

         public ButtonColumnData(JTable table, int column)
         {
             super();
             this.table = table;
             renderButton = new JButton();

             editButton = new JButton();
             
             editButton.setFocusPainted( false );
             editButton.addActionListener( this );

             TableColumnModel columnModel = table.getColumnModel();
             columnModel.getColumn(column).setCellRenderer( this );
             columnModel.getColumn(column).setCellEditor( this );
         }

         public Component getTableCellRendererComponent(
             JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column)
         {
             if (hasFocus)
             {
                 renderButton.setForeground(table.getForeground());
                 renderButton.setBackground(UIManager.getColor("Button.background"));
             }
             else if (isSelected)
             {
                 renderButton.setForeground(table.getSelectionForeground());
                 renderButton.setBackground(table.getSelectionBackground());
            }
             else
             {
                 renderButton.setForeground(table.getForeground());
                 renderButton.setBackground(UIManager.getColor("Button.background"));
             }

             renderButton.setBackground(new java.awt.Color(255, 255, 255));
             renderButton.setBorder(null);
             renderButton.setBorderPainted(false);
             renderButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

             text = (value == null) ? "" : value.toString();
             String imagem1 = text.equals("excluir")?"lixeira":"editar";
             renderButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icones/"+imagem1+".png")));
             return renderButton;
         }

         public Component getTableCellEditorComponent(
             JTable table, Object value, boolean isSelected, int row, int column)
         {
             text = (value == null) ? "" : value.toString();             
             String imagem2 = text.equals("excluir")?"lixeira":"editar";
             editButton.setBackground(new java.awt.Color(255, 255, 255));
             editButton.setBorder(null);
             editButton.setBorderPainted(false);             
             editButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icones/"+imagem2+".png")));
             editButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
             //editButton.setText( text );
             return editButton;
         }

         public Object getCellEditorValue()
         {
             return text;
        }

         public void actionPerformed(ActionEvent e)
         {
             fireEditingStopped();

             switch(table.getSelectedColumn()){
                 case 3:
                     SolicitaConfirmacaoData();                    
                     TabelaData();
                 break;
                 case 2:
                     int id_reg = Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString());
                     obj_Data = bd_Data.getDataID(id_reg);
                    
                     String strdata = new SimpleDateFormat("dd/MM/yyyy").format(obj_Data.getData());
                     String vet[] = strdata.split("/");
                     
                     data_dia.setText(vet[0]);
                     data_mes.setText(vet[1]);
                     data_ano.setText(vet[2]);
                     
                     Data_id.setText(String.valueOf(obj_Data.getData_id()));
                 break;
                 default:
                     kernel.Tela(0, kernel, 0,null,null);
                 break;
             }
             //System.out.println( "DADOS : " + table.getSelectedRow()+" "+" "+table.getSelectedColumn()+" "+table.getColumnCount()+" "+table.getValueAt(table.getSelectedRow(),0));
             
         }
     }
