    //SALVAR Isotopos
    private void SalvarIsotopos(){
        obj_Isotopos   = new Isotopos();
        bd_Isotopos    = new BD_Isotopos(kernel);
        
        if(Isotopos_id.getText().equals("0")){
            obj_Isotopos.setNome(Isotopos_nome.getText());
            if(!Isotopos_nome.getText().equals("")){
                bd_Isotopos.Salvar(obj_Isotopos);
            }
            
        }else{
            obj_Isotopos.setNome(Isotopos_nome.getText());
            obj_Isotopos.setIsotopos_id(Integer.parseInt(Isotopos_id.getText()));
            bd_Isotopos.Atualizar(obj_Isotopos);
        }
        
        Isotopos_nome.setText(""); 
        Isotopos_id.setText("0"); 
        //PREENCHER A TABELA DE DADOS COM AS ÁREAS
        TabelaIsotopos();
    }
    
    //TABELA Isotopos
    public void TabelaIsotopos(){
    
        bd_Isotopos = new BD_Isotopos(kernel);
        Isotopos[] Isotoposs_vetor = null;
        try {
            //VERIFICAR SE É BUSCA OU NÃO        
            Isotoposs_vetor = bd_Isotopos.getIsotoposs();
        } catch (SQLException ex) {
            Logger.getLogger(CadastrarIsotopos.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        String dados[][] = new String[Isotoposs_vetor.length][4];

        Isotopos obj = new Isotopos();
        for(int i=0; i < Isotoposs_vetor.length; i++){
            obj = (Isotopos)Isotoposs_vetor[i];
            dados[i][0] = String.valueOf(obj.getIsotopos_id());
            dados[i][1] = String.valueOf(obj.getNome());            
            dados[i][2] = "editar";
            dados[i][3] = "excluir";
         
        }
      
        
        tabela_Isotopos.setModel(new javax.swing.table.DefaultTableModel(
            dados,
            new String [] {"Cod", "Nome","", ""}){
            Class[] types = new Class [] {
                java.lang.String.class, java.lang.String.class, java.lang.String.class, java.lang.String.class
            };

            public Class getColumnClass(int columnIndex) {
                return types [columnIndex];
            }
        });
      
        tabela_Isotopos.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
        tabela_Isotopos.getColumnModel().getColumn(0).setPreferredWidth(50);
        tabela_Isotopos.getColumnModel().getColumn(1).setPreferredWidth(300);        
        tabela_Isotopos.getColumnModel().getColumn(2).setPreferredWidth(30);
        tabela_Isotopos.getColumnModel().getColumn(3).setPreferredWidth(30);
        
        tabela_Isotopos.setDragEnabled(true);
        sp.setViewportView(tabela_Isotopos);        
        //ButtonColumn buttonColumn0 = new ButtonColumn(table, 0);//VER
        ButtonColumnIsotoposs buttonColumnIsotoposs1 = new ButtonColumnIsotoposs(tabela_Isotopos, 2);//EDITAR
        ButtonColumnIsotoposs buttonColumnIsotoposs2 = new ButtonColumnIsotoposs(tabela_Isotopos, 3);//EXCLUIR
    }
    
    //CLASSE BUTTONCOLUMN IsotoposS
    class ButtonColumnIsotoposs extends AbstractCellEditor
         implements TableCellRenderer, TableCellEditor, ActionListener
     {
         JTable table;
         JButton renderButton;
         JButton editButton;
         String text;

         public ButtonColumnIsotoposs(JTable table, int column)
         {
             super();
             this.table = table;
             renderButton = new JButton();

             editButton = new JButton();
             
             editButton.setFocusPainted( false );
             editButton.addActionListener( this );

             TableColumnModel columnModel = table.getColumnModel();
             columnModel.getColumn(column).setCellRenderer( this );
             columnModel.getColumn(column).setCellEditor( this );
         }

         public Component getTableCellRendererComponent(
             JTable table, Object value, boolean isSelected, boolean hasFocus, int row, int column)
         {
             if (hasFocus)
             {
                 renderButton.setForeground(table.getForeground());
                 renderButton.setBackground(UIManager.getColor("Button.background"));
             }
             else if (isSelected)
             {
                 renderButton.setForeground(table.getSelectionForeground());
                 renderButton.setBackground(table.getSelectionBackground());
            }
             else
             {
                 renderButton.setForeground(table.getForeground());
                 renderButton.setBackground(UIManager.getColor("Button.background"));
             }

             renderButton.setBackground(new java.awt.Color(255, 255, 255));
             renderButton.setBorder(null);
             renderButton.setBorderPainted(false);
             renderButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));

             text = (value == null) ? "" : value.toString();
             String imagem1 = text.equals("excluir")?"lixeira":"editar";
             renderButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icones/"+imagem1+".png")));
             return renderButton;
         }

         public Component getTableCellEditorComponent(
             JTable table, Object value, boolean isSelected, int row, int column)
         {
             text = (value == null) ? "" : value.toString();             
             String imagem2 = text.equals("excluir")?"lixeira":"editar";
             editButton.setBackground(new java.awt.Color(255, 255, 255));
             editButton.setBorder(null);
             editButton.setBorderPainted(false);             
             editButton.setIcon(new javax.swing.ImageIcon(getClass().getResource("/icones/"+imagem2+".png")));
             editButton.setCursor(new java.awt.Cursor(java.awt.Cursor.HAND_CURSOR));
             //editButton.setText( text );
             return editButton;
         }

         public Object getCellEditorValue()
         {
             return text;
        }

         public void actionPerformed(ActionEvent e)
         {
             fireEditingStopped();

             switch(table.getSelectedColumn()){
                 case 3:
                     SolicitaConfirmacaoIsotopos();
                     TabelaIsotopos();
                 break;
                 case 2:
                     int id_reg = Integer.parseInt(table.getValueAt(table.getSelectedRow(),0).toString());
                     obj_Isotopos = bd_Isotopos.getIsotoposID(id_reg);
                     Isotopos_nome.setText(obj_Isotopos.getNome());
                     Isotopos_id.setText(String.valueOf(obj_Isotopos.getIsotopos_id()));
                 break;
                 default:
                     kernel.Tela(0, kernel, 0,null,null);
                 break;
             }
             //System.out.println( "DADOS : " + table.getSelectedRow()+" "+" "+table.getSelectedColumn()+" "+table.getColumnCount()+" "+table.getValueAt(table.getSelectedRow(),0));
             
         }
     }
    private void SolicitaConfirmacaoIsotopos(){
     boolean escolha = false;

     Object[] options = {"Sim",
                    "Não"};
    int resposta = JOptionPane.showOptionDialog(this,
        "Desejas excluir o registro ?",
        "Exclusão",
        JOptionPane.YES_NO_OPTION,
        JOptionPane.WARNING_MESSAGE,
        null,     //do not use a custom Icon
        options,  //the titles of buttons
        options[1]);
     
     if (resposta == 0) {
       bd_Isotopos.Excluir(Integer.parseInt(tabela_Isotopos.getValueAt(tabela_Isotopos.getSelectedRow(),0).toString()));
     }
     
    }

