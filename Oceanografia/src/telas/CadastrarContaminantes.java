    private void SalvarContaminantes(){
        obj_Contaminantes   = new Contaminantes();
        bd_Contaminantes    = new BD_Contaminantes(kernel);
        
        if(Contaminantes_id.getText().equals("0")){
            obj_Contaminantes.setNome(Contaminantes_nome.getText());
            if(!Contaminantes_nome.getText().equals("")){
                bd_Contaminantes.Salvar(obj_Contaminantes);
            }
            
        }else{
            obj_Contaminantes.setNome(Contaminantes_nome.getText());
            obj_Contaminantes.setContaminantes_id(Integer.parseInt(Contaminantes_id.getText()));
            bd_Contaminantes.Atualizar(obj_Contaminantes);
        }
        
        Contaminantes_nome.setText(""); 
        Contaminantes_id.setText("0"); 
        //PREENCHER A TABELA DE DADOS COM AS ÁREAS
        TabelaContaminantes();
    }
    
