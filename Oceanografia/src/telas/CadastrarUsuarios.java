    private void PreencherFormulario(int acao) throws SQLException {
        
           Usuario_id.setText(String.valueOf(acao));               
           obj_Usuario = bd_Usuario.getUsuarioID(acao);
           campo_nome.setText(obj_Usuario.getNome());           
           campo_login.setText(obj_Usuario.getLogin());
           campo_senha.setText(obj_Usuario.getSenha());
           combo_nivel.setSelectedIndex(obj_Usuario.getNivel());           
           
    }

