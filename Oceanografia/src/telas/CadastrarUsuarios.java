    //SALVAR Usuario
    private void Salvar(){
        obj_Usuario    = new Usuario();
        if(Usuario_id.getText().equals("0")){            
            
                obj_Usuario.setNome((campo_nome.getText().equals("")?"":campo_nome.getText()));
                obj_Usuario.setLogin((campo_login.getText().equals("")?"":campo_login.getText()));
                obj_Usuario.setSenha((campo_senha.getText().equals("")?"":campo_senha.getText()));
                obj_Usuario.setNivel(Integer.parseInt((Nivel_id.getText())));

                if(!campo_nome.getText().equals("")){
                    bd_Usuario.Salvar(obj_Usuario);
                }
        }else{            

                obj_Usuario.setUsuario_id(Integer.parseInt(Usuario_id.getText()));
                obj_Usuario.setNome((campo_nome.getText().equals("")?"":campo_nome.getText()));
               
                obj_Usuario.setLogin((campo_login.getText().equals("")?"":campo_login.getText()));
                obj_Usuario.setSenha((campo_senha.getText().equals("")?"":campo_senha.getText()));
                obj_Usuario.setNivel(Integer.parseInt(Nivel_id.getText()));

                bd_Usuario.Atualizar(obj_Usuario);
        }
        
        kernel.Tela(40,null,0,null,null);
        
    }
    
   
    
