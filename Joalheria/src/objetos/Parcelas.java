public class Parcelas {

    private int parcelas_id;
    private String nome;

    
    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the parcelas_id
     */
    public int getParcelas_id() {
        return parcelas_id;
    }

    /**
     * @param parcelas_id the parcelas_id to set
     */
    public void setParcelas_id(int parcelas_id) {
        this.parcelas_id = parcelas_id;
    }

    
}
